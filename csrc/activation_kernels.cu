/**
 * =============================================================================
 * 1. 활성화 함수가 필요한 이유
 * =============================================================================
 *
 * 신경망에서 선형 변환만 쌓으면 아무리 많은 층을 쌓아도 하나의 선형 변환과 같습니다:
 *   W₂(W₁x + b₁) + b₂ = W₂W₁x + W₂b₁ + b₂ = W'x + b'
 *
 * 비선형 활성화 함수를 사이에 넣어야 진정한 "깊은" 학습이 가능합니다:
 *   f(W₂ · f(W₁x + b₁) + b₂) ← f가 비선형이면 더 이상 단순화 불가능
 *
 *
 * [활성화 함수의 발전]
 *
 *   ReLU (2010~)           GELU (2016~)             SiLU/Swish (2017~)
 *   max(0, x)              x·Φ(x)                   x·σ(x)
 *        │                      │                        │
 *   장점: 빠름              장점: 부드러움             장점: 부드럽고
 *   단점: "죽는 뉴런"       단점: erf() 비용           자기 게이팅(self-gating)
 *   (x<0이면 기울기 0)      (근사식 필요)              단점: exp() 필요
 *
 *
 * ─────────────────────────────────────────────────────────────────────────────
 * [GELU (Gaussian Error Linear Unit) — Hendrycks & Gimpel, 2016]
 * ─────────────────────────────────────────────────────────────────────────────
 *
 * 수식: GELU(x) = x · Φ(x)
 *
 * 여기서 Φ(x)는 표준 정규분포(Standard Normal Distribution)의
 * 누적분포함수(CDF, Cumulative Distribution Function)입니다:
 *
 *   Φ(x) = P(X ≤ x) = ∫_{-∞}^{x} (1/√(2π)) · exp(-t²/2) dt
 *
 * 직관적으로 Φ(x)는 "표준 정규분포에서 x 이하의 값이 나올 확률"입니다:
 *   Φ(-∞) = 0  (확률 0%)
 *   Φ(0)  = 0.5 (정확히 반)
 *   Φ(+∞) = 1  (확률 100%)
 *
 * 따라서 GELU(x) = x · Φ(x)의 동작은:
 *   x가 큰 양수 → Φ(x) ≈ 1 → GELU(x) ≈ x     (거의 그대로 통과)
 *   x가 큰 음수 → Φ(x) ≈ 0 → GELU(x) ≈ 0     (억제)
 *   x = 0      → Φ(0) = 0.5 → GELU(0) = 0     (0)
 *
 * [0 근처에서의 동작 — "부분 통과"]
 *
 * GELU의 핵심은 극단(큰 양수/음수)이 아닌, 0 근처의 동작입니다.
 * Φ(x)는 0~1 사이의 연속적인 값을 가지므로, x에 "비율"을 곱하는 효과입니다.
 * ReLU처럼 x=0에서 딱 잘리는 것이 아니라, 점진적으로 부드럽게 전환됩니다.
 *
 * 구체적인 값으로 보면:
 *
 *   x      Φ(x)    GELU(x)=x·Φ(x)   의미
 *   ───    ─────   ───────────────   ─────────────────────
 *  -3.0    0.001       -0.004        거의 0 (억제)
 *  -1.0    0.159       -0.159        x의 약 16%만 통과
 *  -0.5    0.309       -0.154        x의 약 31%만 통과
 *   0.0    0.500        0.000        x 자체가 0
 *   0.5    0.691        0.346        x의 약 69% 통과
 *   1.0    0.841        0.841        x의 약 84% 통과
 *   3.0    0.999        2.997        거의 그대로 통과
 *
 * x=-1이면 원래 값의 16%만 살아남고, x=1이면 84%가 살아남습니다.
 * 이 "부분 통과"가 ReLU와의 결정적 차이입니다:
 *
 *   ReLU(-1) = 0        ← 음수는 무조건 0 (정보 완전 소실)
 *   GELU(-1) = -0.159   ← 음수도 일부 통과 (정보 부분 보존)
 *
 *   ReLU(-0.5) = 0      ← 역시 0
 *   GELU(-0.5) = -0.154 ← 31%가 통과
 *
 * 특히 x ≈ -0.5 ~ 0.5 구간에서 GELU는 x를 약 30~70%만 통과시키는
 * "부드러운 게이트" 역할을 합니다. 이 구간이 학습 초기에 가중치가
 * 작을 때 자주 나타나므로, GELU는 학습 초기 단계에서도 기울기가
 * 골고루 흐르게 하여 ReLU보다 학습이 안정적입니다.
 *
 * "입력 x가 다른 뉴런들의 출력에 비해 얼마나 큰가"에 따라
 * 확률적으로 통과시킬지 억제할지 결정하는 "확률적 게이팅"입니다.
 * 이것은 Dropout의 아이디어와 유사합니다 — Dropout이 랜덤하게
 * 뉴런을 끄는 것처럼, GELU는 값의 크기에 따라 "부드럽게" 끕니다.
 *
 * [GELU의 구현 문제 — Φ(x)와 erf()의 관계]
 *
 * Φ(x)는 적분으로 정의됩니다:
 *   Φ(x) = ∫_{-∞}^{x} (1/√(2π)) · exp(-t²/2) dt
 *
 * 이 적분은 "닫힌 형태(closed-form)"가 존재하지 않습니다.
 * 닫힌 형태란 사칙연산, 거듭제곱, exp, log, sin 같은 기본 함수의
 * 유한한 조합으로 표현할 수 있는 수식을 말합니다.
 *
 * 보통 적분을 배울 때, 적분 결과가 기본 함수로 표현됩니다:
 *   ∫ x² dx = x³/3 + C          ← 결과가 기본 함수(x³)로 표현됨
 *   ∫ cos(x) dx = sin(x) + C    ← 결과가 기본 함수(sin)로 표현됨
 *   ∫ 1/x dx = ln(x) + C        ← 결과가 기본 함수(ln)로 표현됨
 *
 * 이렇게 "적분 결과 = 기본 함수의 조합"으로 쓸 수 있는 것이 닫힌 형태입니다.
 *
 * 하지만 exp(-t²)는:
 *   ∫ exp(-t²) dt = ???          ← exp, sin, log 등 어떤 조합으로도 쓸 수 없음
 *
 * 적분 자체는 "값"으로는 존재합니다 (곡선 아래 면적이니까 숫자는 있음).
 * 하지만 그 값을 나타내는 "공식"이 없습니다.
 * 이것은 수학적으로 증명되어 있습니다 (Liouville의 정리).
 *
 * [닫힌 형태가 없으면 왜 문제인가?]
 *
 * 닫힌 형태가 있으면 공식에 x를 대입하면 끝입니다:
 *   ∫ x² dx = x³/3  →  F(3.0) = 3.0³/3 = 9.0  (즉시 계산 가능)
 *
 * 닫힌 형태가 없으면 "대입할 공식" 자체가 존재하지 않습니다.
 * Φ(3.0)을 구하려면 적분을 직접 수치적으로 근사해야 합니다:
 *   Φ(3.0) = ∫_{-∞}^{3.0} (1/√(2π)) · exp(-t²/2) dt
 *   → 구간을 잘게 쪼개서 사다리꼴/심슨 등으로 근사하거나
 *   → 급수 전개(테일러 급수 등)로 항을 충분히 많이 더하거나
 *   → 비슷한 형태의 다른 함수(tanh 등)로 근사
 *
 * 이 모든 방법은 "정확한 값"이 아니라 "근사값"이고,
 * 정밀도를 높이려면 더 많은 계산이 필요합니다.
 * GPU에서 원소 하나마다 이런 근사를 수행하면 비용이 큽니다.
 *
 * 그래서 수학자들은 이 "공식이 없는 적분"에 별도의 이름을 붙였습니다.
 * π가 3.14159...를 소수점으로 정확히 쓸 수 없어서 별도의 기호 π를
 * 부여한 것처럼, 이 적분의 값에도 이름을 부여한 것입니다:
 *
 *   erf(x) = (2/√π) · ∫_0^x exp(-t²) dt    ← "오차 함수(Error Function)"
 *
 * "오차 함수"라는 이름은 통계학에서 측정 오차의 확률을 계산할 때
 * 이 적분이 반복적으로 등장했기 때문에 붙여졌습니다.
 *
 * [Φ(x)와 erf(x)의 관계 — 변수 치환]
 *
 * Φ(x)와 erf(x)는 변수 치환(t → t/√2)으로 서로 변환됩니다:
 *
 *   Φ(x) = ∫_{-∞}^{x} (1/√(2π)) · exp(-t²/2) dt
 *
 *         = 0.5 + ∫_0^x (1/√(2π)) · exp(-t²/2) dt
 *                 ~~~                                  (대칭성: ∫_{-∞}^0 = 0.5)
 *
 *         = 0.5 + (1/√(2π)) · ∫_0^x exp(-t²/2) dt
 *
 *   u = t/√2로 치환하면 dt = √2·du이고:
 *         = 0.5 + (1/√(2π)) · √2 · ∫_0^{x/√2} exp(-u²) du
 *         = 0.5 + (1/√π) · ∫_0^{x/√2} exp(-u²) du
 *         = 0.5 + 0.5 · (2/√π) · ∫_0^{x/√2} exp(-u²) du
 *         = 0.5 + 0.5 · erf(x/√2)
 *         = 0.5 · (1 + erf(x/√2))
 *
 * 최종 결과:
 *   Φ(x) = 0.5 · (1 + erf(x / √2))
 *
 *
 * [왜 Φ(x)를 erf(x)로 치환하는가?]
 *
 * Φ(x)도 닫힌 형태가 없고, erf(x)도 닫힌 형태가 없는데,
 * 굳이 치환하는 이유는 erf()가 수학/공학/통계에서 워낙 자주 사용되어
 * 이미 대부분의 프로그래밍 언어에 라이브러리 함수로 구현되어 있기 때문입니다.
 * Φ(x)를 직접 수치 적분하는 것보다, erf()로 치환한 뒤 라이브러리 함수를
 * 호출하는 것이 훨씬 빠르고 정확합니다:
 *
 *   방법 1: Φ(x) = ∫_{-∞}^{x} ... dt   → 직접 수치 적분 (느림, 부정확)
 *   방법 2: Φ(x) = 0.5·(1 + erf(x/√2)) → 라이브러리 erf() 호출 (빠름, 정확)
 *
 * 즉 "닫힌 형태는 없지만, 이미 잘 만들어 놓은 구현이 있는 함수"로
 * 변환하는 것이 치환의 실용적 목적입니다.
 *
 *
 * [GELU의 구현 — tanh 근사와 erff()]
 *
 * GELU가 처음 사용되던 시기(BERT, GPT-2)에는 GPU에서 erf()를 빠르게
 * 계산할 방법이 마땅치 않았습니다. 그래서 tanh()를 사용한 근사식이
 * 제안되었습니다:
 *
 *   GELU(x) ≈ 0.5x · (1 + tanh(√(2/π) · (x + 0.044715 · x³)))
 *
 * 이 근사식이 동작하는 이유:
 * - erf(x)와 tanh(x)는 모두 S자 곡선으로 형태가 매우 비슷
 * - 둘 다 원점 대칭, erf(0)=tanh(0)=0, 극한에서 ±1에 수렴
 * - √(2/π) ≈ 0.7979 스케일링과 0.044715·x³ 보정항으로 오차를 최소화
 * - tanh(x) = (exp(x)-exp(-x))/(exp(x)+exp(-x))이므로 exp()만으로 계산 가능
 *   → GPU의 SFU(Special Function Unit)가 exp()를 하드웨어로 빠르게 처리
 * - 최대 오차가 약 0.0003으로 실용적으로 무시할 수 있는 수준
 *
 * 하지만 현재는 CUDA에 erff() 함수가 내장되어 있으므로,
 * 굳이 tanh 근사를 쓸 필요 없이 정확한 erf()를 바로 사용할 수 있습니다:
 *   - C/C++: <cmath>의 erf()
 *   - Python: math.erf()
 *   - CUDA: erff() — GPU에서 최적화된 구현 제공
 *
 * 정리하면:
 *   tanh() 근사: 초기 GPU 환경에서 사용 (BERT, GPT-2 시절)
 *   erff() 직접 호출: 현재 표준 — 정확하고 충분히 빠름
 *
 * [GELU를 사용하는 모델들]
 * BERT, GPT-2, GPT-3 등 초기 대형 언어 모델들이 GELU를 채택했습니다.
 *
 *
 * ─────────────────────────────────────────────────────────────────────────────
 * [SiLU / Swish — Ramachandran et al., 2017 / Elfwing et al., 2018]
 * ─────────────────────────────────────────────────────────────────────────────
 *
 * SiLU와 Swish는 독립적으로 제안되었지만 사실상 같은 함수입니다.
 *
 * [Swish의 탄생 — Google Brain의 자동 탐색]
 * Swish는 Ramachandran et al. (2017, "Searching for Activation Functions")에서
 * 제안되었습니다. 이 논문의 접근법이 독특합니다:
 *
 * 사람이 활성화 함수를 설계하는 대신, 강화학습(Reinforcement Learning)을 사용하여
 * 가능한 수학적 조합들 중에서 최적의 활성화 함수를 "자동 탐색"했습니다.
 * 탐색 공간은 단항 연산(exp, sin, σ, ...)과 이항 연산(+, ×, ...)의 조합이었고,
 * 다양한 태스크에서의 성능을 보상으로 사용했습니다.
 *
 * 탐색 결과 발견된 최고의 활성화 함수:
 *   Swish(x) = x · σ(βx)
 *
 * β는 학습 가능한 파라미터 또는 상수입니다.
 *
 * [SiLU — Swish의 특수한 경우]
 * SiLU (Sigmoid Linear Unit, Elfwing et al., 2018)는 β = 1인 Swish입니다:
 *
 *   SiLU(x) = x · σ(x)     (= Swish-1)
 *
 * "Swish"라는 이름이 더 먼저 유명해졌지만, 수학적으로는 β=1인 경우를
 * 별도로 SiLU라고 부릅니다. 실제로 대부분의 LLM 구현에서 β=1을 사용하므로
 * SiLU = Swish = Swish-1로 혼용됩니다.
 *
 * PyTorch에서도 torch.nn.SiLU()와 F.silu()로 제공하며,
 * 내부적으로 x * sigmoid(x)를 계산합니다.
 *
 * [SiLU의 구현상 장점 — 닫힌 형태(closed-form)]
 *
 * GELU의 Φ(x)는 적분으로 정의되어 닫힌 형태가 없으므로
 * 근사식(tanh 근사 등)으로 계산해야 합니다. (위 GELU 섹션 참조)
 *
 * 반면 SiLU의 σ(x) = 1/(1+exp(-x))는 닫힌 형태 그 자체입니다.
 * σ(3.0)을 구하고 싶으면 공식에 대입하면 됩니다:
 *   σ(3.0) = 1/(1+exp(-3.0)) = 1/(1+0.0498) = 0.9526
 *   → exp() 한 번, 덧셈 한 번, 나눗셈 한 번 — 끝
 *
 * 근사가 아닌 정확한 값이고, 계산 비용이 고정됩니다.
 * 게다가 GPU의 SFU(Special Function Unit)가 exp()를
 * 하드웨어 회로로 빠르게 처리하므로 실행 비용도 낮습니다.
 *
 * [β 파라미터의 의미]
 *   β → 0  : Swish(x) → x/2       (선형에 가까워짐)
 *   β = 1  : Swish(x) = SiLU(x)   (표준 SiLU)
 *   β → ∞  : Swish(x) → ReLU(x)   (ReLU에 수렴)
 *
 * 즉 Swish는 선형 함수와 ReLU 사이를 β로 보간하는 함수 패밀리이며,
 * β=1 (SiLU)이 실험적으로 가장 좋은 성능을 보여 표준이 되었습니다.
 *
 * [GELU vs SiLU 비교]
 *   GELU(x) = x · Φ(x)    (Φ: 정규분포 CDF, erf 기반)
 *   SiLU(x) = x · σ(x)    (σ: 시그모이드, exp 기반)
 *
 * 두 함수의 구조가 매우 유사합니다: "x에 0~1 범위의 게이팅 함수를 곱한다"
 * 차이는 게이팅 함수가 Φ(정규분포 CDF)인지 σ(시그모이드)인지입니다.
 * Φ(x)와 σ(x)의 그래프가 매우 비슷하므로, 실제 출력값도 거의 유사합니다.
 *
 * 그럼에도 SiLU가 선호되는 이유:
 * - σ(x) = 1/(1+exp(-x))는 닫힌 형태(closed-form)로 정확히 계산 가능
 * - Φ(x)는 적분으로 정의되어 근사식이 필요 (erf 또는 tanh 근사)
 * - GPU에서 exp()는 SFU로 빠르게 계산, erf()는 상대적으로 느림
 * - SwiGLU (SiLU + GLU)가 실험적으로 GEGLU (GELU + GLU)보다 약간 우수
 *
 * LLaMA, Mistral 등 최신 LLM은 SiLU를 사용합니다.
 * 특히 LLaMA는 SwiGLU 구조를 채택하여 SiLU를 게이트로 활용합니다.
 *
 *
 * =============================================================================
 * 2. LLaMA MLP 아키텍처 (SwiGLU)
 * =============================================================================
 *
 * LLaMA는 SwiGLU (Shazeer, "GLU Variants Improve Transformer", 2020)를
 * MLP 블록에 사용합니다.
 *
 * [기존 Transformer MLP (GPT 등)]
 *
 *   MLP(x) = W_down · GELU(W_up · x)
 *   - 2개의 선형 변환: W_up (d → 4d), W_down (4d → d)
 *   - 활성화 함수: GELU
 *
 * [LLaMA SwiGLU MLP]
 *
 *   MLP(x) = W_down · ( SiLU(W_gate · x) ⊙ W_up · x )
 *   - 3개의 선형 변환: W_gate, W_up (d → d'), W_down (d' → d)
 *   - SiLU를 게이트로 사용
 *   - ⊙: element-wise 곱셈
 *
 * W_gate는 "어떤 정보를 통과시킬지" 결정하는 게이트(gate) 역할을 합니다.
 * SiLU가 시그모이드를 포함하므로 0~1 범위의 게이팅이 자연스럽게 이루어집니다.
 * W_up은 게이트를 거치지 않고 직접 곱해지므로 정보를 "그대로 전달"하는 역할입니다.
 *
 * [SwiGLU의 장점]
 * - 일반 MLP 대비 약 1% 정확도 향상 (같은 파라미터 수 기준)
 * - 게이트 메커니즘이 정보 흐름을 더 세밀하게 제어
 * - 비용: 선형 변환이 2개 → 3개로 증가 (보통 intermediate_size를 줄여 보상)
 *   LLaMA-7B: d=4096, d'=11008 (원래 4d=16384보다 작아서 총 파라미터 수 비슷)
 *
 *
 * [Fused silu_and_mul 연산]
 *
 * 실제 구현에서는 gate_proj와 up_proj의 출력을 하나의 텐서로 연결합니다:
 *
 *   combined = [gate_proj(x) | up_proj(x)]   → shape: [..., 2*d']
 *   output = silu_and_mul(combined)           → shape: [..., d']
 *
 * silu_and_mul의 동작:
 *   gate = combined[..., :d']       (앞 절반)
 *   up   = combined[..., d':]       (뒷 절반)
 *   output = silu(gate) * up
 *
 * 이렇게 하면:
 * - gate_proj와 up_proj를 하나의 행렬곱으로 합칠 수 있음 (연산 효율)
 * - SiLU와 곱셈을 하나의 커널로 합칠 수 있음 (메모리 효율)
 *
 *
 * =============================================================================
 * 3. SiLU의 수학적 성질
 * =============================================================================
 *
 * [수식]
 *
 * SiLU(x) = x · σ(x) = x / (1 + exp(-x))
 *
 * 여기서 σ(x) = 1 / (1 + exp(-x))는 시그모이드 함수입니다.
 *
 * [도함수]
 * SiLU'(x) = σ(x) + x · σ(x) · (1 - σ(x))
 *          = σ(x) · (1 + x · (1 - σ(x)))
 *
 * ReLU의 도함수가 {0, 1}인 것과 달리, SiLU의 도함수는 부드럽습니다.
 * 특히 x < 0에서도 미세한 기울기가 있어 "죽는 뉴런" 문제가 없습니다.
 *
 * [수학적 성질]
 * - 부드러움(smooth): 모든 점에서 미분 가능 (ReLU의 x=0 꺾임 없음)
 * - 비단조(non-monotonic): x ≈ -1.278에서 최솟값 ≈ -0.278
 * - 자기 게이팅: 입력 자체가 게이트 역할 (σ(x)가 0~1 사이 값)
 *   → 작은 음수는 억제하고, 큰 양수는 거의 그대로 통과
 * - 극한 동작:
 *   x → +∞: σ(x) → 1 이므로 SiLU(x) → x (항등)
 *   x → -∞: σ(x) → 0 이므로 SiLU(x) → 0 (억제)
 *
 * [수치 안정성]
 *
 * SiLU(x) = x · σ(x) = x / (1 + exp(-x))
 *
 * 이 수식은 수학적으로 다른 형태로도 쓸 수 있습니다:
 *   형태 A: x / (1 + exp(-x))       ← exp(-x) 사용
 *   형태 B: x · exp(x) / (1 + exp(x))  ← exp(x) 사용
 *
 * 두 형태는 수학적으로 동일하지만, 구현에서는 형태 A를 사용합니다.
 *
 * 이유: x가 큰 양수일 때 (예: x = 1000)
 *   형태 A: 1000 / (1 + exp(-1000))
 *         = 1000 / (1 + 0)       ← exp(-1000) ≈ 0, 안전
 *         = 1000                 ✓ 정확한 결과
 *
 *   형태 B: 1000 · exp(1000) / (1 + exp(1000))
 *         = 1000 · inf / (1 + inf)
 *         = 1000 · inf / inf     ← inf/inf = NaN!
 *         = NaN                  ✗ 계산 실패
 *
 * x가 큰 음수일 때 (예: x = -1000):
 *   형태 A: -1000 / (1 + exp(1000))
 *         = -1000 / (1 + inf)
 *         = -1000 / inf          ← IEEE 754: 숫자/inf = 0
 *         = 0                    ✓ exp(-x)가 overflow해도 결과는 정확
 *
 * 즉 형태 A는 양수에서 exp(-x)→0이라 안전하고,
 * 음수에서 exp(-x)→inf이지만 분모에 있으므로 결과가 0으로 수렴합니다.
 * 어느 방향이든 NaN이 나오지 않습니다.
 *
 * [GPU에서의 효율]
 * expf()는 GPU의 Special Function Unit (SFU)에서 실행됩니다.
 * SFU는 exp, sin, cos, rsqrt 등 초월 함수를 하드웨어 회로로 빠르게 계산합니다.
 * 따라서 SiLU는 ReLU보다 약간 느리지만, 메모리 바운드 커널에서는 차이가 미미합니다.
 *
 *
 * =============================================================================
 * 4. 메모리 대역폭 분석
 * =============================================================================
 *
 * [silu_and_mul은 메모리 바운드 커널]
 *
 * 연산량 분석 (bf16, d' = 11008, 1개 토큰):
 *   - 입력 읽기: 2 * 11008 * 2바이트 = 44KB
 *   - 출력 쓰기: 11008 * 2바이트 = 22KB
 *   - 총 메모리 이동: 66KB
 *   - 연산: 11008 * (exp 1회 + 나누기 1회 + 곱하기 2회) ≈ 44K FLOPs
 *
 * A100의 메모리 대역폭: ~2TB/s, 연산 처리량: ~312 TFLOPS (bf16)
 *   → 66KB 메모리 전송에 0.033μs 필요
 *   → 44K 연산에 0.00014μs 필요
 *   → 메모리 시간이 연산 시간의 ~240배
 *
 * 따라서 이 커널은 "완전히 메모리 바운드"입니다.
 * 연산 최적화보다 메모리 접근 최적화(벡터화)가 핵심입니다.
 * 벡터화의 구체적인 구현은 activation_kernels.cuh를 참조하세요.
 *
 * [Fused 커널의 메모리 절약 효과]
 * 별도 커널 2개 (silu + elementwise_mul):
 *   44KB(읽기) + 22KB(silu 쓰기) + 22KB(silu 읽기) + 22KB(up 읽기) + 22KB(출력 쓰기) = 132KB
 * Fused 커널 1개 (silu_and_mul):
 *   44KB(읽기) + 22KB(출력 쓰기) = 66KB
 * → 메모리 이동량이 정확히 절반으로 감소 → 메모리 바운드 커널에서 ~2배 속도 향상
 */

#include <torch/all.h>
#include <ATen/cuda/CUDAContext.h>
#include <c10/cuda/CUDAGuard.h>

#include "dispatch_utils.h"
#include "activation_kernels.cuh"


/**
 * =============================================================================
 * 5. PyTorch 래퍼 코드
 * =============================================================================
 *
 * 아래 함수들은 PyTorch 텐서를 받아 CUDA 커널을 호출하는 진입점입니다.
 *
 * - Grid=(num_tokens,): 각 블록이 하나의 토큰(행) 처리
 * - Block=(min(d, 1024),): CUDA 블록당 최대 스레드 수는 1024
 * - num_tokens == 0인 경우 즉시 리턴하여 빈 텐서에 대한 커널 런치를 방지합니다
 * - OptionalCUDAGuard: 입력 텐서가 있는 GPU 디바이스에서 실행되도록 보장
 * - getCurrentCUDAStream: 현재 CUDA 스트림에서 비동기 실행
 */

/**
 * silu - SiLU 활성화 함수 Python 진입점
 *
 * 각 원소에 SiLU(x) = x * sigmoid(x)를 적용합니다.
 *
 * @param out    출력 텐서 [..., d]
 * @param input  입력 텐서 [..., d]
 */
void silu(
    torch::Tensor& out,
    torch::Tensor& input) {

    int d = input.size(-1);
    int64_t num_tokens = input.numel() / d;
    if (num_tokens == 0) return;

    dim3 grid(num_tokens);
    dim3 block(std::min(d, 1024));

    const at::cuda::OptionalCUDAGuard device_guard(device_of(input));
    const cudaStream_t stream = at::cuda::getCurrentCUDAStream();

    VLLM_DISPATCH_FLOATING_TYPES(
        input.scalar_type(), "silu_kernel", [&] {
            lightvllm::activation_kernel<scalar_t, lightvllm::silu_kernel<scalar_t>>
                <<<grid, block, 0, stream>>>(
                    out.data_ptr<scalar_t>(),
                    input.data_ptr<scalar_t>(),
                    d);
        });
}


/**
 * silu_and_mul - Fused SiLU + Mul Python 진입점
 *
 * LLaMA MLP의 게이트 연산: silu(gate) * up
 *
 * 입력의 마지막 차원을 절반으로 나누어:
 *   gate = input[..., :d]   → SiLU 적용
 *   up   = input[..., d:]   → 그대로 곱셈
 *   output = silu(gate) * up
 *
 * @param out    출력 텐서 [..., d]
 * @param input  입력 텐서 [..., 2*d]
 */
void silu_and_mul(
    torch::Tensor& out,
    torch::Tensor& input) {

    int d = input.size(-1) / 2;
    int64_t num_tokens = input.numel() / input.size(-1);
    if (num_tokens == 0) return;

    dim3 grid(num_tokens);
    dim3 block(std::min(d, 1024));

    const at::cuda::OptionalCUDAGuard device_guard(device_of(input));
    const cudaStream_t stream = at::cuda::getCurrentCUDAStream();

    VLLM_DISPATCH_FLOATING_TYPES(
        input.scalar_type(), "silu_and_mul_kernel", [&] {
            lightvllm::act_and_mul_kernel<scalar_t, lightvllm::silu_kernel<scalar_t>>
                <<<grid, block, 0, stream>>>(
                    out.data_ptr<scalar_t>(),
                    input.data_ptr<scalar_t>(),
                    d);
        });
}
